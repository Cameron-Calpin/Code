package main

import (
	"fmt"
	"math/rand"
	"time"
)

func main() {
	rand.Seed(time.Now().UTC().UnixNano())
	a := rand.Intn(10)
	for a < 100 {
		if a%5 == 0 {
			goto done
		}
		a = a*2 + 1
	}
	fmt.Println("Do something when the loop completes normally")
	done:
		fmt.Println("do complicated stuff no matter why we left the loop")
		fmt.Println(a)
}